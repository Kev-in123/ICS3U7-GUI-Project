package Game;

public class World {
  // variables used throughout the class
  protected int worldHeight = 800;
  protected int worldWidth = 1535;

  /**
   * returns the height of the world
   * 
   * @param N/A
   * @return worldHeight
   */
  public int getHeight() {
    return worldHeight;
  }

  /**
   * returns the width of the world
   * 
   * @param N/A
   * @return worldWidth
   */
  public int getWidth() {
    return worldWidth;
  }

}
